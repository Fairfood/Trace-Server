for id in ids:
    card = NodeCard.objects.get(card_id=id["card_id"])
    hex_id = hex(int(id["card_id"])).lstrip("0x").rstrip("L")
    rev_hex = "".join(map(str.__add__, hex_id[-2::-2], hex_id[-1::-2])).upper()
    existing_cards = NodeCard.objects.filter(card_id=rev_hex)
    if existing_cards.count() > 1:
        print("################## mutiple itme for ", id)
        break
    if existing_cards:
        existing_card = existing_cards.first()
        existing_card.fairid = id["fairid"]
        existing_card.save()
        card.delete()
        continue

    card.card_id = rev_hex
    card.save()
